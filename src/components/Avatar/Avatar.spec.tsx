import * as React from 'react';
import { createUniDriverFactory } from 'wix-ui-test-utils/uni-driver-factory';
import { isUniEnzymeTestkitExists } from 'wix-ui-test-utils/enzyme';
import { isUniTestkitExists } from 'wix-ui-test-utils/vanilla';
import { mount } from 'enzyme';
import { avatarDriverFactory } from './Avatar.driver';
import { Avatar } from './';
import { avatarTestkitFactory } from '../../testkit';
import { avatarTestkitFactory as enzymeAvatarTestkitFactory } from '../../testkit/enzyme';

describe('Avatar', () => {
  const createDriver = createUniDriverFactory(avatarDriverFactory);

  it('should render', async () => {
    const driver = createDriver(<Avatar />);
    expect(await driver.exists()).toBe(true);
  });

  describe('testkit', () => {
    it('should exist', async () => {
      expect(
        await isUniTestkitExists(<Avatar />, avatarTestkitFactory, {
          dataHookPropName: 'data-hook',
        }),
      ).toBe(true);
    });
  });

  describe('enzyme testkit', () => {
    it('should exist', async () => {
      expect(
        await isUniEnzymeTestkitExists(
          <Avatar />,
          enzymeAvatarTestkitFactory,
          mount,
          {
            dataHookPropName: 'data-hook',
          },
        ),
      ).toBe(true);
    });
  });
});
